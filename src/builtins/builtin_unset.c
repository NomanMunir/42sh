#include "shell.h"

int builtin_unset(char **argv, t_shell *shell) {
  int i;

  i = 1;
  while (argv[i]) {
    var_unset(shell, argv[i]);
    env_unset(shell, argv[i]);
    i++;
  }
  return (0);
}
